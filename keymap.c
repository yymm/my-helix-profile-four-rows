void render_status(struct CharacterMatrix *matrix) {

  static char vim[4][6] = {
    {0x1b,0x1c,0x1d,0x1e,0x1f,0},
    {0x3b,0x3c,0x3d,0x3e,0x3f,0},
    {0x5b,0x5c,0x5d,0x5e,0x5f,0},
    {0x7b,0x7c,0x7d,0x7e,0x7f,0}};
  static char apple[2][6] = {
    {0x9c,0x9d,0x9e,0x9f,0x20,0},
    {0xbc,0xbd,0xbe,0xbf,0x20,0}};
  static char win[2][6] = {
    {0x98,0x99,0x9a,0x9b,0x20,0},
    {0xb8,0xb9,0xba,0xbb,0x20,0}};
  static char layer_label[11] = {
    0xd6,0xd7,0xd8,0xd9,0xda,0xdb,0xdc,0xdd,0xde,0xdf,0
  };
  // // l-up, r-up, l-btm, r-btm, l,   r,   matrial
  // // 0x97, 0xb6, 0xb7,  0x96,  0x95,0xb5,0xd5
  static char layer_container[2][11] = {
    {0x20,0xb7,0x95,0x95,0x95,0x95,0x95,0x95,0x97,0x20,0},
    {0x20,0x96,0xb5,0xb5,0xb5,0xb5,0xb5,0xb5,0xb6,0x20,0}
  };
  static char layer_pos[4][11] = {
    {0x20,0x20,0x20,0x20,0xd5,0xd5,0x20,0x20,0x20,0x20,0},
    {0x20,0x20,0x20,0x20,0x20,0x20,0xd5,0xd5,0x20,0x20,0},
    {0x20,0x20,0xd5,0xd5,0x20,0x20,0x20,0x20,0x20,0x20,0},
    {0x20,0x20,0xd5,0xd5,0x20,0x20,0xd5,0xd5,0x20,0x20,0},
  };

  matrix_write(matrix, layer_label);
  matrix_write_P(matrix, PSTR("     "));
  matrix_write(matrix, vim[0]);
  matrix_write_P(matrix, PSTR("\n"));

  matrix_write(matrix, layer_container[0]);
  if(keymap_config.swap_lalt_lgui==false){
    matrix_write(matrix, apple[0]);
  } else {
    matrix_write(matrix, win[0]);
  }
  matrix_write(matrix, vim[1]);
  matrix_write_P(matrix, PSTR("\n"));

  switch (layer_state) {
    case L_BASE:
      matrix_write(matrix, layer_pos[0]);
      break;
    case L_RAISE:
      matrix_write(matrix, layer_pos[1]);
      break;
    case L_LOWER:
      matrix_write(matrix, layer_pos[2]);
      break;
    case L_ADJUST:
    case L_ADJUST_TRI:
      matrix_write(matrix, layer_pos[3]);
      break;
    default:
      matrix_write_P(matrix, PSTR("           "));
      break;
  }
  if(keymap_config.swap_lalt_lgui==false){
    matrix_write(matrix, apple[1]);
  } else {
    matrix_write(matrix, win[1]);
  }
  matrix_write(matrix, vim[2]);
  matrix_write_P(matrix, PSTR("\n"));

  matrix_write(matrix, layer_container[1]);
  matrix_write_P(matrix, PSTR("     "));
  matrix_write(matrix, vim[3]);
}


void iota_gfx_task_user(void) {
  struct CharacterMatrix matrix;

#if DEBUG_TO_SCREEN
  if (debug_enable) {
    return;
  }
#endif

  matrix_clear(&matrix);
  if(is_master){
    render_status(&matrix);
  }else{
    render_logo(&matrix);
  }
  matrix_update(&display, &matrix);
}

